//! AudioCapture implementation for Apple platforms via raw CoreAudio FFI.
//!
//! Builds the input AudioUnit directly using objc2-audio-toolbox C functions.
//! The fix for kAudioUnitErr_InvalidPropertyValue (-10851) on physical iPhone:
//! coreaudio-rs calls AudioUnitInitialize inside its constructor; on device,
//! any property set after initialize (EnableIO, stream format) fails. Here we
//! configure FIRST, then initialize.
//!
//! # Raw open() sequence (all before AudioUnitInitialize)
//!
//! a. AudioComponentDescription { type=Output, subType=RemoteIO|HALOutput, mfr=Apple }
//! b. AudioComponentFindNext(null, &desc) → component
//! c. AudioComponentInstanceNew(component, &mut unit)  ← NO initialize yet
//! d. macOS only: warm-up init → CurrentDevice (scope Global, element 0) →
//!    EnableIO input=1 (scope Input=1, element 1) →
//!    DisableIO output=0 (scope Output=2, element 0) → uninit.
//!    Both CurrentDevice and EnableIO require an initialized unit (-10851
//!    otherwise). Both survive uninitialize.
//!    iOS RemoteIO: EnableIO/DisableIO on the uninitialized unit, no CurrentDevice.
//! e. kAudioUnitProperty_StreamFormat on (scope Output=2, element 1): interleaved F32
//! f. kAudioUnitProperty_ShouldAllocateBuffer = 0 on (scope Output, element 1)
//! g. Set kAudioOutputUnitProperty_SetInputCallback (scope Global, element 0)
//! h. AudioUnitInitialize(unit)  ← NOW, after all config
//! i. AudioOutputUnitStart(unit)

